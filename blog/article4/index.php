<h2 class="article__main-title">献综述写作六种类型</h2>

<p>
  在上一期了解了Literature Review写作常见问题后，这一期，我们要来简单知道文献综述的几种类型，针对不同学科，专业领域，选择合适类型的文献综述，能帮助写作者更好地表达自己的思路。
</p>

