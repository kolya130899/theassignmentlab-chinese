// SWITCH FAQS TABS
function switchFaqsTabs() {
  $(".faq-nav-item").on("click", function () {
    if (!$(this).hasClass("active")) {
      $(".faq-nav-item.active").removeClass("active");
      $(this).addClass("active");

      var tabName = $(this).find("p").text();

      printFaqs(tabName);
    }
  });
}
// TOGGLE FAQs
function toggleFaq() {
  $(document).on("click", ".faq-item", function () {
    $(this).toggleClass("active");
    $(this).find(".faq-item__body").slideToggle(300);
  });
}

// PRINT FAQS ELEMENTS
function printFaqs(tabName) {
  faqs.forEach(function (item) {
    if (item.tab === tabName) {
      var faqElements = item.faqs.map(function (item) {
        var element =
          '<div class="faq-item"><h3>' +
          item.title +
          '</h3><div class="faq-item__body">' +
          item.text +
          "</div></div>";

        return element;
      });

      var elements = faqElements.join("");
      $(".faq-items__wrapper").html(elements);
    }
  });
}

$(document).ready(function () {
  printFaqs(faqs[0].tab);
  switchFaqsTabs();
  toggleFaq();
});
